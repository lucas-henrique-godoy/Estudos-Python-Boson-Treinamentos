# Set: Conjuntos/coleções não ordenadas de valores únicos usados para armazenar multiplos itens dentro de um objeto.
# - Apenas armazenam itens não duplicados e suportam operações matemáticas sobre conjuntos: União, Intersecção, etc.
# - Não é possível modificar os itens dentro de um set, mas conseguimos adicionar novos elementos. Portanto o set em si, o conjunto é mutável.
